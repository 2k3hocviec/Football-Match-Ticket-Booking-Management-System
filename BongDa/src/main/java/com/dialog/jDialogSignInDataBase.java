    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        String name = jTextFieldNameDatabase.getText();
        String passWord = String.valueOf(jPassWordDatabase.getPassword());
        String server = jTextFieldServerDatabase.getText();
        DataBaseConnect.setDatabase(server, passWord, name);
        String str = DataBaseConnect.testConnection();
        switch (str) {
            case "Execellent":
                JOptionPane.showMessageDialog(this, "Kết nối thành công", "Inforamtion", JOptionPane.OK_OPTION);
                this.dispose();
                break;
            case "Fall":
                JOptionPane.showMessageDialog(this, "Kết nối thất bại", "Inforamtion", JOptionPane.OK_OPTION);
                break;
            case "Loi SQL":
                JOptionPane.showMessageDialog(this, "Lỗi cơ sở dư liệu", "Inforamtion", JOptionPane.OK_OPTION);
                break;
            case "Driver not found":
                JOptionPane.showMessageDialog(this, "Không có driver", "Inforamtion", JOptionPane.OK_OPTION);
                break;
            default:
                break;
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

