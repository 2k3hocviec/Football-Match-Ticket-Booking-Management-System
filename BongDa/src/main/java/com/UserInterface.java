    void openMenuBar () {
        new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i = 0; i <= weightMenuBar; ++i) {
                    MenuAnimation.setSize(i, heightMenuBar);
                    try {
                        Thread.sleep(2);
                    } catch (InterruptedException ex) {
                        System.getLogger(UserInterface.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                    }
                }
            }
        }).start();
    }
    
    void closeMenuBar () {
        new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i = weightMenuBar; i >= 0; --i) {
                    MenuAnimation.setSize(i, heightMenuBar);
                    try {
                        Thread.sleep(2);
                    } catch (InterruptedException ex) {
                        System.getLogger(UserInterface.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                    }
                }               
            }
        }).start();
    }
    
