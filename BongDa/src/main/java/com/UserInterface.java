    private void jLabelToCloseThisTabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelToCloseThisTabMouseClicked
        int index = TabMain.getSelectedIndex();
        String title = TabMain.getTitleAt(index);
        boolean check = false;
        
        if (title.equals("Book Seat") || title.equals("Confirm and Payment") || title.equals("Ticket")) {
            check = true;
        }
        if (index != 0 && check == false) {
            TabMain.remove(index);
        }
    }//GEN-LAST:event_jLabelToCloseThisTabMouseClicked

    public void deleteTab() {
        int index = TabMain.getSelectedIndex();
        if (index != 0) {
            TabMain.remove(index);
        }
    }
    
    public void deleteTabByComponent(Component component) {
        int index = TabMain.indexOfComponent(component);
        if (index != -1) {
            TabMain.remove(index);
        }
    }

    
    private void jLabelOpenAndCloseMenuBarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelOpenAndCloseMenuBarMouseClicked
        if (!checkOpenMenuBar) {
            openMenuBar();
            TabMain.setVisible(false);
            checkOpenMenuBar = true;
            changIconForOpenAndCloseMenuBar(jLabelOpenAndCloseMenuBar, "/left32.png");
        } else {
            closeMenuBar();
            TabMain.setVisible(true);
            checkOpenMenuBar = false;
            changIconForOpenAndCloseMenuBar(jLabelOpenAndCloseMenuBar, "/menu32.png");
        }
    }//GEN-LAST:event_jLabelOpenAndCloseMenuBarMouseClicked

